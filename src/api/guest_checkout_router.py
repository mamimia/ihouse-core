# ---------------------------------------------------------------------------
# Token verification (multi-use: verify but do NOT consume)
# ---------------------------------------------------------------------------

def _verify_guest_checkout_token(
    token: str, db: Any = None
) -> tuple[Optional[dict], Optional[str]]:
    """
    Verify a GUEST_CHECKOUT access token.
    IMPORTANT: Multi-use verification. We do NOT call validate_and_consume()
    because guests may need to re-open the portal multiple times.
    Instead: HMAC + expiry (via verify_access_token) + DB not-revoked check.

    Phase 1065B fallback: if the token does not validate as GUEST_CHECKOUT
    (for example when the guest portal CTA passes the GUEST_PORTAL token),
    try resolving it as a GUEST_PORTAL token via resolve_guest_token_context.
    On success, synthesise a compatible claims dict so the rest of the route
    works identically regardless of which token type was supplied.

    Returns (claims_dict, None) on success, or (None, error_str) on failure.
    """
    from services.access_token_service import TokenType, verify_access_token

    # --- Path 1: proper GUEST_CHECKOUT token (normal flow) ---
    claims = verify_access_token(
        token=token,
        expected_type=TokenType.GUEST_CHECKOUT,
    )
    if claims:
        # DB revocation check (without marking used_at)
        if db:
            try:
                token_hash = _hash_token(token)
                res = (
                    db.table("access_tokens")
                    .select("id, revoked_at")
                    .eq("token_hash", token_hash)
                    .eq("token_type", TokenType.GUEST_CHECKOUT.value)
                    .limit(1)
                    .execute()
                )
                rows = res.data or []
                if rows and rows[0].get("revoked_at"):
                    return None, "This checkout link has been revoked. Contact your host for a new link."
            except Exception:
                pass  # Non-fatal: HMAC already validated
        return claims, None

    # --- Path 2: GUEST_PORTAL token fallback (Phase 1065B) ---
    # Inline verification with one unique diagnostic code per sub-step.
    # Bypasses resolve_guest_token_context() to identify the exact failure point.
    import time as _time_mod, hmac as _hmac_mod, base64 as _b64_mod, hashlib as _hl_mod
    try:
        from services.guest_token import _get_secret, _decode_token, _sign, is_guest_token_revoked
    except ImportError as _ie:
        return None, f"[diag-P2-IMPORT] {_ie}"

    try:
        _secret = _get_secret()
    except RuntimeError as _re:
        return None, f"[diag-P2-SECRET] IHOUSE_GUEST_TOKEN_SECRET not set: {_re}"

    _decoded = _decode_token(token)
    if _decoded is None:
        return None, f"[diag-P2-DECODE] no dot separator or bad base64. token[:30]={token[:30]!r}"

    _message, _provided_sig = _decoded
    _expected_sig = _sign(_message, _secret)
    if not _hmac_mod.compare_digest(_provided_sig, _expected_sig):
        return None, f"[diag-P2-HMAC] HMAC mismatch. provided[:8]={_provided_sig[:8]!r} expected[:8]={_expected_sig[:8]!r}"

    _parts = _message.split(":", 2)
    if len(_parts) != 3:
        return None, f"[diag-P2-FORMAT] message has {len(_parts)} parts, expected 3. msg={_message[:40]!r}"

    _booking_ref, _guest_email, _exp_str = _parts
    try:
        _exp = int(_exp_str)
    except ValueError:
        return None, f"[diag-P2-EXP-VAL] exp_str not an int: {_exp_str!r}"

    _now = int(_time_mod.time())
    if _exp < _now:
        return None, f"[diag-P2-EXPIRED] token expired {_now - _exp}s ago (exp={_exp}, now={_now})"

    if not _booking_ref:
        return None, f"[diag-P2-NOREF] booking_ref is empty in token message"

    logger.info("_verify_guest_checkout_token: path2 success booking_ref=%s", _booking_ref)
    return {
        "token_type": "guest_checkout",
        "entity_id":  _booking_ref,
        "email":      _guest_email or "",
        "exp":        _exp,
        "_via_portal_token": True,
    }, None





