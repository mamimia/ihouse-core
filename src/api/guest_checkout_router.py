# POST /guest-checkout/{token}/step/{step_key}
# Public, token-gated. Mark a single checkout step complete.
# ===========================================================================

@router_portal.post(
    "/guest-checkout/{token}/step/{step_key}",
    summary="Submit a guest checkout step (Phase 1045)",
    status_code=200,
)
async def submit_checkout_step(
    token: str,
    step_key: str,
    body: dict = {},
    client: Optional[Any] = None,
) -> JSONResponse:
    """
    Mark an individual checkout step as complete.

    Valid step_key values: confirm_departure, key_handover, feedback
    Body for 'feedback': { rating?: 1-5, comment?: string }
    Body for others: (none required)

    Idempotent — re-submitting overwrites previous value.
    """
    if step_key not in _CHECKOUT_STEPS:
        return make_error_response(
            status_code=400, code="UNKNOWN_STEP",
            extra={
                "detail": f"'{step_key}' is not a valid checkout step.",
                "valid_steps": _CHECKOUT_STEPS,
            },
        )

    db = client or _get_db()
    claims, err = _verify_guest_checkout_token(token, db=db)
    if err:
        return make_error_response(status_code=401, code="TOKEN_INVALID", extra={"detail": err})

    booking_id = claims.get("entity_id")
    if not booking_id:
        return make_error_response(status_code=401, code="TOKEN_INVALID",
                                   extra={"detail": "Invalid token."})

    booking = _get_booking_for_portal(db, booking_id)
    if not booking:
        return make_error_response(status_code=404, code=ErrorCode.NOT_FOUND,
                                   extra={"detail": "Booking not found."})

    tenant_id = booking["tenant_id"]
    steps = booking.get("guest_checkout_steps_completed") or {}
    now = _now_iso()

    # Build step detail payload
    step_detail: dict = {"completed_at": now}
    extra_booking_update: dict = {}  # Phase 1065B: additional booking_state fields

    if step_key == "feedback":
        rating = body.get("rating")
        comment = body.get("comment") or ""
        if rating is not None:
            try:
                r = int(rating)
                if 1 <= r <= 5:
                    step_detail["rating"] = r
            except (ValueError, TypeError):
                pass
        if comment:
            step_detail["comment"] = str(comment)[:500]
    elif step_key == "confirm_departure":
        step_detail["confirmed_by_guest"] = True
    elif step_key == "key_handover":
        method = str(body.get("method") or "confirmed")
        step_detail["method"] = method[:50]
    elif step_key == "ac_lights":
        step_detail["confirmed"] = True
        note = str(body.get("note") or "")[:200]
        if note:
            step_detail["note"] = note
    elif step_key == "doors_locked":
        step_detail["confirmed"] = True
        note = str(body.get("note") or "")[:200]
        if note:
            step_detail["note"] = note
    elif step_key == "contact_confirm":
        phone = str(body.get("phone") or "").strip()[:50] or None
        email = str(body.get("email") or "").strip()[:200] or None
        if not phone and not email:
            return make_error_response(
                status_code=400, code="CONTACT_REQUIRED",
                extra={"detail": "Please provide a phone number or email address for follow-up."},
            )
        step_detail["phone"] = phone
        step_detail["email"] = email
        if phone:
            extra_booking_update["guest_checkout_contact_phone"] = phone
        if email:
            extra_booking_update["guest_checkout_contact_email"] = email

    steps[step_key] = step_detail

    update_payload = {
        "guest_checkout_steps_completed": steps,
        "updated_at_ms": _now_ms(),
        **extra_booking_update,
    }

    try:
        db.table("booking_state").update(update_payload).eq("booking_id", booking_id).eq("tenant_id", tenant_id).execute()
    except Exception as exc:
        logger.error("guest_checkout: step update failed: %s", exc)
        return make_error_response(status_code=500, code="SERVER_ERROR",
                                   extra={"detail": "Failed to save step. Please try again."})

    return JSONResponse(status_code=200, content={
        "status":          "step_completed",
        "step_key":        step_key,
        "steps_completed": list(steps.keys()),
    })


