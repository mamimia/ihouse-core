def _get_identity(authorization: str) -> Optional[dict]:
    """Verify JWT and return identity dict, or None on failure."""
    try:
        from services.auth_service import verify_jwt_and_get_identity
        token = authorization.replace("Bearer ", "").strip()
        return verify_jwt_and_get_identity(token)
    except Exception:
        return None


def _assert_generate_role(identity: dict) -> Optional[JSONResponse]:
    role = identity.get("role", "")
    # Workers can have multiple roles; check canonical roles then delegated
    if role in _GENERATE_ROLES:
        return None
    # Delegated worker capabilities
    worker_roles = set((identity.get("worker_roles") or "").split(","))
    if worker_roles & {"CHECKOUT", "CHECKIN"}:
        return None
    return make_error_response(
        status_code=403, code="PERMISSION_DENIED",
        extra={"detail": f"Role '{role}' cannot generate guest checkout tokens."},
    )


